package projeto;

public abstract class Pessoa {

	private String name;
	private int age;
	
	//NAME
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	
	//AGE
	public int getAge() {
		return age;
	}
	public void setAge(int age) {
		this.age= age;
	}
	
	

}
